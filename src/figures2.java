public class figures2 {
	
	public static void main(String[] args) {
		egg();
		System.out.println();
		teacup();
		System.out.println();
		stopsign();
		System.out.println();
		hat();
	}
	
	/*
	 * Draw egg
	 */
	public static void egg() {
		System.out.println("  ______");
		System.out.println(" /      \\");
		System.out.println("/        \\");
		System.out.println("\\        /");
		System.out.println(" \\______/");
	}
	
	/*
	 * Draw tea cup
	 */
	public static void teacup() {
		System.out.println("\\        /");
		System.out.println(" \\______/");
		System.out.println("+--------+");
	}
	
	/*
	 * Draw stop sign
	 */
	public static void stopsign() {
		System.out.println("  ______");
		System.out.println(" /      \\");
		System.out.println("/        \\");
		System.out.println("|  STOP  |");
		System.out.println("\\        /");
		System.out.println(" \\______/");
	}
	
	/*
	 * draw hat
	 */
	public static void hat() {
		System.out.println("  ______");
		System.out.println(" /      \\");
		System.out.println("/        \\");
		System.out.println("+--------+");
	}
}
